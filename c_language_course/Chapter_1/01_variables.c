#include <stdio.h>

int main() {

    int number;   // variable declaration
    number = 10; // variable initialisation

    // both variable declaration and initialisation can be done in one statement

    int number_1 = 20;

    // printing values of both the variables

    printf("%d\n", number);
    printf("%d\n", number_1);
    
    return 0;

}