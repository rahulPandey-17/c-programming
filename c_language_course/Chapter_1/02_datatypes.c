#include <stdio.h>

int main() {

    int a = 15;
    float b = 20.5;
    char c = 'r';

    // printing the values of the above data-types

    printf("Value of integer data-type is %d\n", a);
    printf("Value of float data-type is %f\n", b);
    printf("Value of character data-type is %c\n", c);
    
    return 0;

}