#include <stdio.h>

int main() {

    // we can declare and initialize multiple variables in a single line

    int a = 10, b = 20, c = 30, d = 40;

    // printing the values of the above variables

    printf("Value of a is %d\n", a);
    printf("Value of b is %d\n", b);
    printf("Value of c is %d\n", c);
    printf("Value of d is %d\n", d);

    return 0;

}